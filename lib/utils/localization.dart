  String translate(String key) {
    final translations = _localizedValues[locale.languageCode];
    if (translations == null) return key;
    final keys = key.split('.');
    dynamic value = translations;
    for (final k in keys) {
      if (value is Map) {
        value = value[k];
      } else {
        return key;
      }
    }
    return value?.toString() ?? key;
  }

